# Funcion para calcular la resta de 2 numeros
def resta(a, b):
    return a - b

# Funcion para dividir 2 numeros
def dividir(a, b):
    return a / b

#Funcion para sumar 2 numeros
def suma(a, b):
    return a + b

#Funcion para multiplicar 2 numeros
def prod(a, b):
    return a * b

#Funcion para calcular el residuo de 2 numeros
def mod(a, b):
    return a % b